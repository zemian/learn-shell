#!/usr/bin/env bash
#
# Shebang is the first comment line in script that tell OS what program to use
# to execute the current script. The 'env' command will search the given
# command in the user PATH. This is so that you don't have to hardcode program
# path such as "/bin/bash", which not always the latest version!
#
# Making script excutable before run:
#
#   chmod a+x shebang.sh
#   ./shebang.sh
#
bash --version
