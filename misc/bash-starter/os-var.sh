#!/usr/bin/env bash

# OS specific support (must be 'true' or 'false').
cygwin=false
msys=false
darwin=false
nonstop=false
case "`uname`" in
  CYGWIN* )
    cygwin=true
    ;;
  Darwin* )
    darwin=true
    ;;
  MINGW* )
    msys=true
    ;;
  NONSTOP* )
    nonstop=true
    ;;
esac

echo "cygwin=$cygwin"
echo "msys=$msys"
echo "darwin=$darwin"
echo "nonstop=$nonstop"
